from basic_operators import add, subtract, multiply, divide

# Examples of basic operations
print(add(5.678, 3.456))        # Output: 9.13 (rounded to 2 decimal places)
print(subtract(5.678, 3.456))   # Output: 2.22 (rounded to 2 decimal places)
print(multiply(5.678, 3.456))   # Output: 19.63 (rounded to 2 decimal places)
print(divide(5.678, 0))         # Shows error popup and returns None
print(divide(5.678, 3.456))     # Output: 1.64 (rounded to 2 decimal places)
